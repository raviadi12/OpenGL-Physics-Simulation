std::vector<glm::vec3> generateRandomCubePositions(int numCubes, float minX, float maxX, float minY, float maxY, float minZ, float maxZ) {
    std::vector<glm::vec3> cubePositions;
    std::random_device rd;
    std::mt19937 gen(rd());
    std::uniform_real_distribution<> disX(minX, maxX);
    std::uniform_real_distribution<> disY(minY, maxY);
    std::uniform_real_distribution<> disZ(minZ, maxZ);

    for (int i = 0; i < numCubes; ++i) {
        glm::vec3 pos(disX(gen), disY(gen), disZ(gen));
        cubePositions.push_back(pos);
    }
    return cubePositions;
}

void framebuffer_size_callback(GLFWwindow* window, int width, int height);
